package variabels;

public class datatype3 {
	// number types default value is zero
	byte b;
	short s;
	int i;
	long l;
	// decimal types default values 0.0
	float f;
	double d;
	
	char c;         //empty space
	boolean bol;    //false
	
	public static void main(String[] args) {
		  datatype3 dt3=new datatype3();
		  
		  System.out.println(dt3.b);
		  System.out.println(dt3.s);
		  System.out.println(dt3.i);
		  System.out.println(dt3.l);
		  
		  System.out.println(dt3.f);
		  System.out.println(dt3.d);
		  
		  System.out.println(dt3.c);
		  System.out.println(dt3.bol);
		  
	}
	
	

}
