package variabels;

public class datatype4 {
	Byte b;                  //objective datatypes or classes    default value is null
	Short s;
	Integer i;
	Long l;
	
	Float f;
	Double d;
	
	Character c;
	Boolean bool;
	
	String str;
	product prod;
	
	public static void main(String[] args) {
		datatype4 dt4=new datatype4();
		
		System.out.println(dt4.b);
		System.out.println(dt4.s);
		System.out.println(dt4.i);
		System.out.println(dt4.l);
		
		System.out.println(dt4.f);
		System.out.println(dt4.d);
		
		System.out.println(dt4.c);
		System.out.println(dt4.bool);
		
		System.out.println(dt4.str);
		
		System.out.println(dt4.prod);
		
	}

}
